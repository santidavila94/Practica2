package clases;

/**
 *
 * @author santi
 */
public class UsuariosAsiduos {
    
    private float total;
    private Usuario usuario;


    /**
     * @return the total
     */
    public float getTotal() {
        return total;
    }

    /**
     * @param total the total to set
     */
    public void setTotal(float total) {
        this.total = total;
    }

    /**
     * @return the usuario
     */
    public Usuario getUsuario() {
        return usuario;
    }

    /**
     * @param usuario the usuario to set
     */
    public void setUsuario(Usuario usuario) {
        this.usuario = usuario;
    }
    
    /**
     * metodo que suma la cantidad de un usuario
     * @param cantidad 
     */
    public void sumarImporte(float cantidad){
        this.total += cantidad;
    }
    
}
